/*********************************************************************************************************//**
  * @brief  Buzzer function 1.
  * @retval None
  ***********************************************************************************************************/
void Buzzer_Fun1(void)
{
  /* Bee 4 times, 3 kHz, active 50 ms, inactive 50 ms                                                       */
  Buzzer_Init(0);
  Buzzer_Start(4, 3000, 50, 50);
  while (Buzzer_IsFinish() == FALSE);
}

